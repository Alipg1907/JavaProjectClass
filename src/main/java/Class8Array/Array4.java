package Class8Array;

public class Array4 {
    public static void main(String[] args) {
        String [] fruit={"Apple","Orange","Banana","Kiwi","Pineapple"};
        System.out.println(fruit.length);
        for (int i = 0; i < fruit.length  ; i++) {
            System.out.println(fruit[i]);
        }
    }
}


