package Review2;





//if statements
// If statements are part of the conditional constructs in java.
//They help us tht can be executed based on a condition.
// they are many variation of if statements
//1)if 2)if-else 3)else-if 4)Nested if
//Why do we need them?
//Without the if statements programs will always generate same output no matter what.
//
//if(condition){
// something that we want to execute if condition is true
//} else{
// something that we want to execute if condition is false
// }
//note Whenever it is possible write code with else-if and multiple if conditions
// Always go with else-if statements.


public class IfStatements {
    public static void main(String[] args) {
        boolean condition = true;
        if (condition) {
            System.out.println(1);
        } else {
            System.out.println(2);
        }
    }
}
