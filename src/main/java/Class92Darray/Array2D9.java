package Class92Darray;

public class Array2D9 {
    public static void main(String[] args) {
        int[][] numbers = {
                {10, 20, 30, 40},
                {15, 30, 35, 45},
                {16, 20, 32, 22}};
        for (int row = 0; row < numbers.length; row++) {
            System.out.println(numbers[1][row]);

        }

    }
}
