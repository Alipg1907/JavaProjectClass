package Class7;

public class NestedClass15 {
    public static void main(String[] args) {

        for (int i = 1; i <= 4; i++) {
            for (int j = 1; j <= 6; j++) {
                if (i == 2 && j == 3) {
                    System.out.println(" ");
                } else {
                    System.out.print("#");
                }

            }
            System.out.println();
        }
    }
}



