package EnhancedLoop;

public class ReplacingLoop11Practice {
    public static void main(String[] args) {
        int[] nums = {10, 77, 25, 56, 60, 20, -10, -5};

        for (int i = 0; i < nums[i]; i++) {

            if (nums[i] < 0) {
                nums[i] = 0;
            }
            System.out.println(nums[i]);
        }

        }
    }





